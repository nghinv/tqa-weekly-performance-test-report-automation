public class BoundaryObject {
	private double resTop;
	private double resUpper;
	private double resLower;
	private double resBottom;
	
	private double thruTop;
	private double thruUpper;
	private double thruLower;
	private double thruBottom;
	public double getResTop() {
		return resTop;
	}
	public void setResTop(double resTop) {
		this.resTop = resTop;
	}
	public double getResUpper() {
		return resUpper;
	}
	public void setResUpper(double resUpper) {
		this.resUpper = resUpper;
	}
	public double getResLower() {
		return resLower;
	}
	public void setResLower(double resLower) {
		this.resLower = resLower;
	}
	public double getResBottom() {
		return resBottom;
	}
	public void setResBottom(double resBottom) {
		this.resBottom = resBottom;
	}
	public double getThruTop() {
		return thruTop;
	}
	public void setThruTop(double thruTop) {
		this.thruTop = thruTop;
	}
	public double getThruUpper() {
		return thruUpper;
	}
	public void setThruUpper(double thruUpper) {
		this.thruUpper = thruUpper;
	}
	public double getThruLower() {
		return thruLower;
	}
	public void setThruLower(double thruLower) {
		this.thruLower = thruLower;
	}
	public double getThruBottom() {
		return thruBottom;
	}
	public void setThruBottom(double thruBottom) {
		this.thruBottom = thruBottom;
	}	
	
}
